class Solution {
    public Node connect(Node root) {
        Node cur = root;
        
        if (root == null) 
            return null;
        
        while (cur != null) {
            Node dum = new Node(0);
            Node tail = dum;
            
            while (cur != null) {
                if (cur.left != null) {
                    tail.next = cur.left;
                    tail = tail.next;
                }
                if (cur.right != null) {
                    tail.next = cur.right;
                    tail = tail.next;
                }
                cur = cur.next;
            }
            cur = dum.next;
        }
        return root;
    }
}